// Вспомогательная: получить значение по ключу вида "nav.catalog"
export function getTranslation(lang, key) {
  const keys = key.split('.');
  let value = translations[lang] || translations.ru;
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) return translations.ru ? getFallback(translations.ru, keys) : key;
  }
  return typeof value === 'string' ? value : key;
}

function getFallback(obj, keys) {
  let v = obj;
  for (const k of keys) {
    v = v?.[k];
    if (v === undefined) return keys.join('.');
  }
  return typeof v === 'string' ? v : keys.join('.');
}
